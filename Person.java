public class Person {


    private String name;
    private int roll;

    Person(String name,int roll)
    {
        this.setName(name);
        this.setRoll(roll);
    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getRoll() {
        return roll;
    }

    public void setRoll(int roll) {
        this.roll = roll;
    }




}
